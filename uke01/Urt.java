public class Urt {
    private String plante;

    public Urt (String plante) {
        this.plante = plante;
    }

    public void settPlante(String plante) {
        this.plante = plante;
    }

    public String hentPlante() {
        return this.plante;
    }

    public String toString() {
        return this.plante;
    }
    

}